package com.netfinworks.rest.annotation;

import com.netfinworks.rest.convert.IParamConvert;
import com.netfinworks.rest.enums.BodyAs;

/**
 * Body注解数据对象
 * @author bigknife
 *
 */
public class BodyAnnotationData {
	private String encoding;
	private Class<? extends IParamConvert> converter;
	private BodyAs as;
	private String converterRef;

	/**
	 * @return the encoding
	 */
	public String getEncoding() {
		return encoding;
	}

	/**
	 * @param encoding
	 *            the encoding to set
	 */
	public void setEncoding(String encoding) {
		this.encoding = encoding;
	}

	/**
	 * @return the paramConvert
	 */
	public Class<? extends IParamConvert> getConverter() {
		return converter;
	}

	/**
	 * @param paramConvert
	 *            the paramConvert to set
	 */
	public void setConverter(Class<? extends IParamConvert> paramConvert) {
		this.converter = paramConvert;
	}

	/**
	 * @return the as
	 */
	public BodyAs getAs() {
		return as;
	}

	/**
	 * @param as
	 *            the as to set
	 */
	public void setAs(BodyAs as) {
		this.as = as;
	}

	/**
	 * @return the converterRef
	 */
	public String getConverterRef() {
		return converterRef;
	}

	/**
	 * @param converterRef
	 *            the converterRef to set
	 */
	public void setConverterRef(String converterRef) {
		this.converterRef = converterRef;
	}

}
