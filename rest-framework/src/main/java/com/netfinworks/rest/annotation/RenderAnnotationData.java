package com.netfinworks.rest.annotation;

import com.netfinworks.rest.render.IRender;

/**
 * @author bigknife
 *
 */
public class RenderAnnotationData {
	private Class<? extends IRender> render;
	private String renderRef;
	
	private Class<? extends IRender> exceptionRender;
	private String exceptionRenderRef;
	

	/**
	 * @return the renderRef
	 */
	public String getRenderRef() {
		return renderRef;
	}

	/**
	 * @param renderRef the renderRef to set
	 */
	public void setRenderRef(String renderRef) {
		this.renderRef = renderRef;
	}

	/**
	 * @return the render
	 */
	public Class<? extends IRender> getRender() {
		return render;
	}

	/**
	 * @param render the render to set
	 */
	public void setRender(Class<? extends IRender> render) {
		this.render = render;
	}

	/**
	 * @return the exceptionRender
	 */
	public Class<? extends IRender> getExceptionRender() {
		return exceptionRender;
	}

	/**
	 * @param exceptionRender the exceptionRender to set
	 */
	public void setExceptionRender(Class<? extends IRender> exceptionRender) {
		this.exceptionRender = exceptionRender;
	}

	/**
	 * @return the exceptionRenderRef
	 */
	public String getExceptionRenderRef() {
		return exceptionRenderRef;
	}

	/**
	 * @param exceptionRenderRef the exceptionRenderRef to set
	 */
	public void setExceptionRenderRef(String exceptionRenderRef) {
		this.exceptionRenderRef = exceptionRenderRef;
	}
}
